#include<iostream>
using namespace std;
int main()
{
    double base,height,area;
    cout<<"Enter the base of the Right angled triangle : ";
    cin>>base;
    cout<<"Enter the height of the Right angled triangle : ";
    cin>>height;
    area=(base*height)/2;
    cout<<"The area of the given triangle is : "<<area<<endl;
    return main();
}